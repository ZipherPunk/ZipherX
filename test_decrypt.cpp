int main() {
    unsigned char dhsecret[32];
    unsigned char pk_d[32];

    printf("Testing with old Zclassic librustzcash:\n\n");

    // First, let's derive ak, nk, and ivk from ask/nsk using old librustzcash
    unsigned char ak[32], nk[32], ivk_derived[32];

    librustzcash_ask_to_ak(ask_key, ak);
    librustzcash_nsk_to_nk(nsk_key, nk);
    librustzcash_crh_ivk(ak, nk, ivk_derived);

    printf("ak from old librustzcash: ");
    for (int i = 0; i < 4; i++) printf("%02x", ak[i]);
    printf("...\n");

    printf("IVK from old librustzcash: ");
    for (int i = 0; i < 32; i++) printf("%02x", ivk_derived[i]);
    printf("\n");

    printf("IVK we are using:         ");
    for (int i = 0; i < 32; i++) printf("%02x", ivk[i]);
    printf("\n\n");

    bool ivk_match = (memcmp(ivk, ivk_derived, 32) == 0);
    printf("IVKs match: %s\n\n", ivk_match ? "✅ YES" : "❌ NO");

    // First verify derived IVK produces correct pk_d
    if (!librustzcash_ivk_to_pkd(ivk_derived, diversifier, pk_d)) {
        printf("ERROR: ivk_to_pkd failed!\n");
        return 1;
    }

    printf("pk_d from ivk_to_pkd: ");
    for (int i = 0; i < 32; i++) printf("%02x", pk_d[i]);
    printf("\n");

    // Now compute shared secret using derived IVK
    if (!librustzcash_sapling_ka_agree(epk, ivk_derived, dhsecret)) {
        printf("ERROR: ka_agree failed!\n");
        return 1;
    }

    printf("Shared secret (dhsecret): ");
    for (int i = 0; i < 32; i++) printf("%02x", dhsecret[i]);
    printf("\n");

    printf("\nFirst 4 bytes: [0x%02x, 0x%02x, 0x%02x, 0x%02x]\n",
           dhsecret[0], dhsecret[1], dhsecret[2], dhsecret[3]);

    // Now compute KDF - need to include sodium for BLAKE2b
    // KDF_Sapling uses BLAKE2b with personalization "Zcash_SaplingKDF"
    // input = dhsecret || epk (64 bytes)

    // Compute KDF using BLAKE2b
    unsigned char kdf_input[64];
    memcpy(kdf_input, dhsecret, 32);
    memcpy(kdf_input + 32, epk, 32);

    unsigned char personalization[crypto_generichash_blake2b_PERSONALBYTES] = {};
    memcpy(personalization, "Zcash_SaplingKDF", 16);

    unsigned char K[32];
    if (crypto_generichash_blake2b_salt_personal(K, 32,
                                                 kdf_input, 64,
                                                 NULL, 0,
                                                 NULL,
                                                 personalization) != 0) {
        printf("ERROR: KDF failed!\n");
        return 1;
    }

    printf("\nKDF key (K): ");
    for (int i = 0; i < 32; i++) printf("%02x", K[i]);
    printf("\n");

    // Print KDF input for debugging
    printf("\nKDF input (dhsecret || epk):\n");
    for (int i = 0; i < 64; i++) {
        printf("%02x", kdf_input[i]);
        if (i == 31) printf("\n");
    }
    printf("\n");

    // Now attempt decryption with ChaCha20Poly1305
    unsigned char cipher_nonce[crypto_aead_chacha20poly1305_IETF_NPUBBYTES] = {};
    unsigned char plaintext[564]; // 580 - 16 (auth tag)
    unsigned long long plaintext_len;

    int result = crypto_aead_chacha20poly1305_ietf_decrypt(
        plaintext, &plaintext_len,
        NULL,
        enc_ciphertext, 580,
        NULL, 0,
        cipher_nonce, K);

    if (result != 0) {
        printf("\n❌ Decryption FAILED! Auth tag mismatch.\n");
        printf("This means either the key is wrong or the ciphertext is for a different recipient.\n");
    } else {
        printf("\n✅ Decryption SUCCESS!\n");
        printf("Plaintext length: %llu bytes\n", plaintext_len);
        printf("First 32 bytes: ");
        for (int i = 0; i < 32 && i < (int)plaintext_len; i++) {
            printf("%02x", plaintext[i]);
        }
        printf("\n");

        // Parse note plaintext
        // Byte 0: lead byte
        // Bytes 1-11: diversifier
        // Bytes 12-19: value (8 bytes little endian)
        if (plaintext_len >= 20) {
            printf("\nLead byte: 0x%02x\n", plaintext[0]);
            printf("Diversifier: ");
            for (int i = 1; i <= 11; i++) printf("%02x", plaintext[i]);
            printf("\n");

            uint64_t value = 0;
            for (int i = 0; i < 8; i++) {
                value |= ((uint64_t)plaintext[12 + i]) << (i * 8);
            }
            printf("Value: %llu zatoshi (%.8f ZCL)\n", value, value / 100000000.0);
        }
    }

    return 0;
}
